package Algorithms.Easy;


public class SolveMeFirst {

    public static void main(String[] args) {
        int res = solveMeFirst(5, 6);
        System.out.println("Sum: " + res);

    }

    static int solveMeFirst(int a, int b) {
        return a + b;
    }
}


